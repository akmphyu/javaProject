public void populateMonthlyReport(String startDate, String endDate){
        DefaultTableModel model = (DefaultTableModel) jTable_dailyReport.getModel();
        ArrayList<Voucher> voucher = (ArrayList<Voucher>) DAOConnection.getDao().getMonthlyReport(startDate,endDate);
        Object rowData[] = new Object[4];
        for(int i = 0; i<voucher.size();i++){
            rowData[0] = voucher.get(i).getItemName();
            rowData[1] = voucher.get(i).getItemQuantity();
            rowData[2] = voucher.get(i).getItemPrice();
            rowData[3] = voucher.get(i).getTotalCost();
          
            model.addRow(rowData);
        }
        
    }
