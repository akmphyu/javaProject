    public boolean verifyFields(){
        String fname = jTextField_fullname.getText();
        String username = jTextField_username.getText();
        String pass1 = String.valueOf(jPasswordField.getPassword());
        String pass2 = String.valueOf(jConfirmedPasswordField.getPassword());
        String phone = jTextField_phone.getText();
        String position = jComboBox_position.getSelectedItem().toString();
        if(fname.trim().equals("") || username.trim().equals("") || pass1.trim().equals("")|| pass2.trim().equals("")
        || phone.trim().equals("")){
            JOptionPane.showMessageDialog(null, "One or more fields are empty", "Empty Fields", 2);
            return false;
        }else if(!pass1.equals(pass2)){
            JOptionPane.showMessageDialog(null, "Password doesn't match", "Confirmed Password", 2);
            return false;
        }else{
            return true;
        }
    }
    
