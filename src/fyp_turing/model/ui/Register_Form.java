    private void jButton_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registerActionPerformed
        // TODO add your phandling code here:
        String fname = jTextField_fullname.getText();
        String username = jTextField_username.getText();
        String pass1 = String.valueOf(jPasswordField.getPassword());
        String pass2 = String.valueOf(jConfirmedPasswordField.getPassword());
        String phone = jTextField_phone.getText();
        String position = jComboBox_position.getSelectedItem().toString();

        if(verifyFields()){
            if(!DAOConnection.getDao().checkUsername(username)){
                User user = new User(fname,username,pass1,phone,position);
                DAOConnection.getDao().insertUser(user);
                Login_Form form = new Login_Form();
                    form.setVisible(true);
                    form.pack();
                    form.setLocationRelativeTo(null);
                    this.dispose();
            }
        }
    }//GEN-LAST:event_jButton_registerActionPerformed

