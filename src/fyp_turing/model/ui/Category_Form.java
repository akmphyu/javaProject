    private void jTable_catMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_catMouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable_cat.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable_cat.getModel();
        Integer cat_id = Integer.valueOf(model.getValueAt(selectedRow, 0).toString());
        String cat_Name = model.getValueAt(selectedRow, 1).toString();
        cat = new Category(cat_id,cat_Name);
        jTextField_fullname.setText(cat_Name);
    }//GEN-LAST:event_jTable_catMouseClicked

