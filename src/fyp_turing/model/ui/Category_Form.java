    private void insert_categoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insert_categoryActionPerformed
        // TODO add your phandling code here:
        String fname = jTextField_fullname.getText();
        Category category = new Category(null, fname);
        if(!fname.trim().equals("")){
            DAOConnection.getDao().insertCategory(category);
            jTextField_fullname.setText("");
        }else{
            JOptionPane.showMessageDialog(null,"Please type new category" );
        }
        populateCategory();
    }//GEN-LAST:event_insert_categoryActionPerformed
    public void populateCategory(){
        DefaultTableModel model = (DefaultTableModel) jTable_cat.getModel();
           model.setRowCount(0);
         ArrayList<Category> category = (ArrayList<Category>) DAOConnection.getDao().getCategory();
        Object rowData[] = new Object[2];
        for(int i = 0; i<category.size();i++){
            rowData[0] = category.get(i).getId();
            rowData[1] = category.get(i).getName();
           
            model.addRow(rowData);
        }
    }
    private void update_categoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_categoryActionPerformed
        // TODO add your handling code here:
        String fname = jTextField_fullname.getText();
         if(!fname.trim().equals("")){
            DAOConnection.getDao().updateCategory(cat,fname);
          
        }else{
            JOptionPane.showMessageDialog(null,"Please select category that you want to update" );
        }
         populateCategory();
    }//GEN-LAST:event_update_categoryActionPerformed

